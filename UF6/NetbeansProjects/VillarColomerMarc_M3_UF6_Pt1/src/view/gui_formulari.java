    public void plenaTaula() {

        this.jtCodi.setText(String.valueOf(v.getCodi()));
        this.jtNom.setText(v.getNom());

        this.jComboBox1.setSelectedIndex(v.getCategoria().getCodi() - 1);

        this.jtRating.setText(String.valueOf(v.getRating()));
        this.jtClub.setText(v.getClub());

        if (this.jRadioButton1.getText().equals(v.getTipusVaixell())) {
            this.jRadioButton1.setSelected(true);
        } else if (this.jRadioButton2.getText().equals(v.getTipusVaixell())) {
            this.jRadioButton2.setSelected(true);
        } else {
            this.jRadioButton3.setSelected(true);
        }
        if (v.isSenior()) {
            this.cbSenior.setSelected(true);
        } else {
            this.cbSenior.setSelected(false);
        }
        this.jtTemps.setText(String.valueOf(v.getTempsReal()));

    }

