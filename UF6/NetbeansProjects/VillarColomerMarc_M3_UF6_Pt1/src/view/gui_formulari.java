private Vaixell creaVaixell() {
        try {
            int codi = Integer.parseInt(this.jtCodi.getText());
            String nom = this.jtNom.getText();
            Categoria cat = new Categoria();
            cat.setCodi(this.jComboBox1.getSelectedIndex());
            cat.setNom(this.jComboBox1.getItemAt(this.jComboBox1.getSelectedIndex()));
            double rating = Double.parseDouble(this.jtRating.getText());
            String club = this.jtClub.getText();
            String tipusVaixell;

            if (this.jRadioButton1.isSelected()) {
                tipusVaixell = this.jRadioButton1.getText();
            } else if (this.jRadioButton2.isSelected()) {
                tipusVaixell = this.jRadioButton2.getText();
            } else {
                tipusVaixell = this.jRadioButton3.getText();
            }
            boolean senior = this.cbSenior.isSelected();
            double tempsReal = Double.parseDouble(this.jtTemps.getText());

            Vaixell va = new Vaixell(codi, nom, cat, rating, club, tipusVaixell, senior, tempsReal);
            return va;
        } catch (Exception e) {
            return null;
        }
    }
    private void jbAfegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAfegirActionPerformed
        // TODO add your handling code here

        Vaixell va = this.creaVaixell();
        try {

            if (this.v == null) {
                va.getCategoria().setCodi(va.getCategoria().getCodi() + 1);
                ctl.afegirVaixell(va);
                JOptionPane.showMessageDialog(this, "Vaixell afegit correctament");
            } else {

                ctl.updateVaixell(va);
                JOptionPane.showMessageDialog(this, "Vaixell modificat correctament");
            }

        } catch (SQLException ex) {
            //  Logger.getLogger(gui_formulari.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error");
        } finally {
            this.dispose();
        }
    }//GEN-LAST:event_jbAfegirActionPerformed

